/**
 * Represents cards in the player's hands, and the cards in the solution.
 * @author Daniel Anastasi
 *
 */
public class Card{
	/**
	 * The six weapon cards.
	 * @author Daniel Anastasi
	 *
	 */
	public enum WEAPON{
		ROPE, DAGGER, CANDLESTICK, REVOLVER, LEADPIPE, SPANNER;
	}
	
	/**
	 * The nine room cards.
	 * @author Daniel Anastasi
	 *
	 */
	public enum ROOM{
		HALL, DINING, BILLIARD, CONSERVATORY, BALL, LIBRARY, STUDY, LOUNGE, KITCHEN;
	}
	
	/**
	 * The six character cards.
	 * @author Daniel Anastasi
	 *
	 */
	public enum CHAR{
		SCARLET, MUSTARD, WHITE, PLUM, GREEN, PEACOCK;
	}
}
